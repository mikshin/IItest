function multi_particles(particle_block_class, particle_config) {
    var blocks = document.getElementsByClassName(particle_block_class);



    for (var i = 0; i < blocks.length; i++) {
        blocks[i].setAttribute('id', particle_block_class + i);
        particlesJS(particle_block_class + i, particle_config);
    }
};

